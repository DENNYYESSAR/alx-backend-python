#!/usr/bin/env python3
"""
Module for converting a float to a string representation.

This module contains a function that takes a float and returns its string
representation.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The float to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
